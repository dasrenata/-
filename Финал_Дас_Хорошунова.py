RISK_THRESHOLD = opt_thr

hr_score = pd.DataFrame({
    "risk_prob": y_prob,
    "risk_category": pd.cut(y_prob,
        bins=[0, RISK_THRESHOLD*0.3, RISK_THRESHOLD, min(RISK_THRESHOLD*2,0.999), 1.0],
        labels=["Низкий","Умеренный","Высокий","Критический"], include_lowest=True),
    "in_risk_zone": (y_prob >= RISK_THRESHOLD).astype(int),
    "actual": y_te.values,
})

n_risk  = hr_score["in_risk_zone"].sum()
n_found = hr_score[(hr_score["in_risk_zone"]==1) & (hr_score["actual"]==1)].shape[0]
n_pos   = hr_score["actual"].sum()

print(f"Порог риска: {RISK_THRESHOLD:.3f}")
print(f"Всего сотрудников в тест-выборке: {len(hr_score):,}")
print(f"В зоне риска: {n_risk:,} ({n_risk/len(hr_score)*100:.1f}%)")
print(f"Реально уволившихся: {n_pos:,}")
print(f"Найдено моделью: {n_found:,} ({n_found/max(n_pos,1)*100:.1f}% от реально уволившихся)")
print(f"\nРаспределение по категориям:")
print(hr_score["risk_category"].value_counts().to_string())

fig, axes = plt.subplots(1, 2, figsize=(13, 4))
hr_score["risk_prob"].hist(bins=60, ax=axes[0], color="#2E6DA4", edgecolor="white")
axes[0].axvline(RISK_THRESHOLD, color="red", ls="--", label=f"Порог {RISK_THRESHOLD:.3f}")
axes[0].set(title="Распределение вероятностей риска", xlabel="P(увольнения)")
axes[0].legend()

cats = ["Низкий","Умеренный","Высокий","Критический"]
hr_score["risk_category"].value_counts().reindex(cats).plot(
    kind="bar", ax=axes[1], color=["#27AE60","#F39C12","#E74C3C","#922B21"], edgecolor="white")
axes[1].set(title="Категории риска"); axes[1].tick_params(axis="x", rotation=15)
plt.tight_layout()
plt.show()

