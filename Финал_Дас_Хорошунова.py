"""## 4. Feature Engineering

- -1 в event-признаках означает «события не было» — расщепляем на _flag (было/не было) и _days_log (давность события)
- Возраст и стаж переводятся из дней в годы
- Индекс хаоса: количество организационных изменений за последние 90 дней
- Эффект домино: число уволившихся коллег по бизнес-линии за последние 4 недели
- Разрывы в стаже/возрасте между сотрудником и руководителями
- Все агрегаты строятся **внутри групп по employee_id** с сортировкой по дате — без утечки данных между сотрудниками
"""

df = df.sort_values(["employee_id","dt"]).reset_index(drop=True)
grp = df.groupby("employee_id", sort=False)

# 1. Порядковый номер наблюдения сотрудника
df["weeks_observed"] = grp.cumcount()

# 2. Возраст и стаж → годы (отрицательный стаж → NaN, без удаления строк)
for prefix in ["","fr_","ar_"]:
    bd, hd = prefix+"birth_date", prefix+"hire_date"
    if bd in df.columns:
        df[prefix+"age_yrs"] = pd.to_numeric(df[bd], errors="coerce") / 365.25
    if hd in df.columns:
        raw = pd.to_numeric(df[hd], errors="coerce")
        df[prefix+"tenure_yrs"] = raw.clip(lower=0) / 365.25
        df.loc[raw < 0, prefix+"tenure_yrs"] = np.nan

# 3. Признаки по стажу
df["tenure_bucket"] = pd.cut(
    df["tenure_yrs"].fillna(-1),
    bins=[-2, 0, 0.5, 1, 2, 3, 5, 10, 100],
    labels=[0,1,2,3,4,5,6,7]).astype(float)
df["tenure_lt1"] = (df["tenure_yrs"].fillna(99) < 1).astype(float)

# 4. Пол → бинарный
for col in ["gender_ccode","fr_gender_ccode","ar_gender_ccode"]:
    if col in df.columns:
        df[col] = (df[col].astype(str).str.strip() == "М").astype(float)

# 5. Разрывы со руководителями
for pfx, label in [("fr_","fr"),("ar_","ar")]:
    for attr in ["age_yrs","tenure_yrs"]:
        e_col, m_col = attr, pfx+attr
        if e_col in df.columns and m_col in df.columns:
            df[f"gap_{label}_{attr}"] = df[m_col] - df[e_col]

# 6. Временные признаки
df["month"]   = df["dt"].dt.month
df["quarter"] = df["dt"].dt.quarter

# 7. Event-признаки: flag + days_log
all_events = EVENT_COLS + ["fr_"+e for e in EVENT_COLS] + ["ar_"+e for e in EVENT_COLS]
for col in all_events:
    if col not in df.columns: continue
    vals = pd.to_numeric(df[col], errors="coerce").fillna(-1)
    df[col+"_flag"]     = (vals > 0).astype(float)
    df[col+"_recent_90"] = ((vals > 0) & (vals <= 90)).astype(float)
    df[col+"_days_log"] = np.log1p(vals.clip(lower=0))

# 8. Индекс-хаоса (организационная нестабильность, изменения в коллективе)
for label, events in {"employee": EVENT_COLS,
                      "fr": ["fr_"+e for e in EVENT_COLS],
                      "ar": ["ar_"+e for e in EVENT_COLS]}.items():
    cols90 = [e+"_recent_90" for e in events if e+"_recent_90" in df.columns]
    df[f"chaos_{label}"] = df[cols90].sum(axis=1) if cols90 else 0.0
df["chaos_total"] = df["chaos_employee"] + df["chaos_fr"] + df["chaos_ar"]

# 9. Конкретные триггеры
adm_days = "employee_headadmin_uk_change_days_log"
if adm_days in df.columns:
    raw_adm = pd.to_numeric(df["employee_headadmin_uk_change"], errors="coerce")
    df["new_manager_recent_30"] = ((raw_adm > 0) & (raw_adm <= 30)).astype(float)

frf_days = "fr_employee_headfunc_uk_change"
if frf_days in df.columns:
    raw_frf = pd.to_numeric(df[frf_days], errors="coerce")
    df["fr_func_changed_60"] = ((raw_frf > 0) & (raw_frf <= 60)).astype(float)

# 10. Лаговые и скользящиепризнаки (по 27 ключевым: amount>50% покрытия, *_days, tenure_yrs)
lag_base = [c for c in HIGH_COV] + [c+"_days_log" for c in EVENT_COLS if c+"_days_log" in df.columns] + ["tenure_yrs"]
lag_base = [c for c in lag_base if c in df.columns]

for col in lag_base:
    df[col+"_lag1w"] = grp[col].shift(1)
    df[col+"_lag4w"] = grp[col].shift(4)
    df[col+"_roll4w"] = grp[col].transform(lambda x: x.shift(1).rolling(4, min_periods=1).mean())
    df[col+"_delta4w"] = df[col] - grp[col].shift(4)

# 11. Эффект домино — увольнения коллег по бизнес-линии
df = df.sort_values(["staffbusinessline_name","dt"])
df["colleagues_fired_last_4w"] = (
    df.groupby("staffbusinessline_name")[TARGET]
    .transform(lambda x: x.shift(1).rolling(4, min_periods=1).sum())
    .fillna(0))
df = df.sort_values(["employee_id","dt"]).reset_index(drop=True)

gc.collect()
print(f"\nИтоговый размер после FE: {df.shape}")
print(f"Признаков создано: {df.shape[1] - 28}")

"""## 5. Разделение данных - Time-Based Split

Обучение на данных **до** TEST_START, тест — **после**. Это имитирует
реальную эксплуатацию: модель обучена на прошлом, оценивается на будущем
"""

mask_tr = df["dt"] < pd.to_datetime(TEST_START)
mask_te = df["dt"] >= pd.to_datetime(TEST_START)

print(f"Граница: {TEST_START}")
print(f"Train: {mask_tr.sum():,} строк ({df.loc[mask_tr,'dt'].min().date()} → {df.loc[mask_tr,'dt'].max().date()})")
print(f"Test:  {mask_te.sum():,} строк ({df.loc[mask_te,'dt'].min().date()} → {df.loc[mask_te,'dt'].max().date()})")
print(f"Train flg=1: {df.loc[mask_tr,TARGET].sum():,} ({df.loc[mask_tr,TARGET].mean()*100:.2f}%)")
print(f"Test  flg=1: {df.loc[mask_te,TARGET].sum():,} ({df.loc[mask_te,TARGET].mean()*100:.2f}%)")

assert mask_tr.sum() > 0 and mask_te.sum() > 0, "ОШИБКА: train или test пустой"
assert df.loc[mask_tr,TARGET].nunique() == 2 and df.loc[mask_te,TARGET].nunique() == 2, \
       "ОШИБКА: в одной из выборок только один класс"


# OHE для staffbusinessline_name
tc   = df.loc[mask_tr,"staffbusinessline_name"].value_counts()
rare = tc[tc < 200].index
df["staffbusinessline_name"] = df["staffbusinessline_name"].where(
    ~df["staffbusinessline_name"].isin(rare), "Прочее")
train_cats = sorted(df.loc[mask_tr,"staffbusinessline_name"].dropna().unique())
df["staffbusinessline_name"] = pd.Categorical(df["staffbusinessline_name"], categories=train_cats)
df = pd.get_dummies(df, columns=["staffbusinessline_name"], drop_first=True, dtype=float)

DROP = [TARGET,"employee_id","dt","birth_date","hire_date",
        "fr_birth_date","fr_hire_date","ar_birth_date","ar_hire_date"] + \
       EVENT_COLS + ["fr_"+e for e in EVENT_COLS] + ["ar_"+e for e in EVENT_COLS]
FEAT = [c for c in df.columns if c not in DROP and df[c].dtype != object]

med = df.loc[mask_tr, FEAT].median()
X_tr = df.loc[mask_tr, FEAT].fillna(med).reset_index(drop=True)
X_te = df.loc[mask_te, FEAT].fillna(med).reset_index(drop=True)
y_tr = df.loc[mask_tr, TARGET].reset_index(drop=True)
y_te = df.loc[mask_te, TARGET].reset_index(drop=True)

print(f"\nПризнаков (после FE и OHE): {len(FEAT)}")
print(f"X_tr: {X_tr.shape}  X_te: {X_te.shape}")
print(f"NaN в X_tr: {X_tr.isnull().sum().sum()}  NaN в X_te: {X_te.isnull().sum().sum()}")

SCALE_POS = float((y_tr==0).sum() / max((y_tr==1).sum(),1))
print(f"scale_pos_weight: {SCALE_POS:.1f}")

del df; gc.collect()

scaler  = StandardScaler()
X_tr_sc = scaler.fit_transform(X_tr)
X_te_sc = scaler.transform(X_te)

"""## 6. Базовые модели

Пять моделей с фиксированными (не оптимизированными) гиперпараметрами —
baseline для последующего сравнения с Optuna-оптимизацией.

Метрики: **ROC-AUC**, **PR-AUC** (основная при дисбалансе), **F1, Recall, Precision** при пороге 0,5
"""

results = []
models  = {}

def eval_model(name, m, Xtr, Xte):
    m.fit(Xtr, y_tr)
    prob = m.predict_proba(Xte)[:,1]
    pred = m.predict(Xte)
    r = {"model": name,
         "ROC_AUC":   round(roc_auc_score(y_te, prob), 4),
         "PR_AUC":    round(average_precision_score(y_te, prob), 4),
         "F1":        round(f1_score(y_te, pred, zero_division=0), 4),
         "Recall":    round(recall_score(y_te, pred, zero_division=0), 4),
         "Precision": round(precision_score(y_te, pred, zero_division=0), 4)}
    models[name] = (m, Xte)
    results.append(r)
    print(f"  {name:<22} ROC={r['ROC_AUC']}  PR={r['PR_AUC']}  "
          f"F1={r['F1']}  Recall={r['Recall']}  Precision={r['Precision']}")
    return r

print("Обучение базовых моделей:\n")

eval_model("LogReg",
    LogisticRegression(C=0.1, max_iter=2000, class_weight="balanced",
                       random_state=RANDOM_STATE, n_jobs=-1),
    X_tr_sc, X_te_sc)

eval_model("DecisionTree",
    DecisionTreeClassifier(max_depth=8, min_samples_leaf=20,
                           class_weight="balanced", random_state=RANDOM_STATE),
    X_tr, X_te)

eval_model("RandomForest",
    RandomForestClassifier(n_estimators=200, max_depth=12, min_samples_leaf=10,
                           class_weight="balanced", n_jobs=-1, random_state=RANDOM_STATE),
    X_tr, X_te)
gc.collect()

eval_model("XGBoost",
    xgb.XGBClassifier(n_estimators=400, learning_rate=0.05, max_depth=6,
                       scale_pos_weight=SCALE_POS, tree_method="hist",
                       eval_metric="aucpr", random_state=RANDOM_STATE,
                       n_jobs=-1, verbosity=0),
    X_tr, X_te)
gc.collect()

eval_model("LightGBM",
    lgb.LGBMClassifier(n_estimators=400, learning_rate=0.05, max_depth=6,
                       is_unbalance=True, random_state=RANDOM_STATE,
                       n_jobs=-1, verbose=-1),
    X_tr, X_te)
gc.collect()

res_df = pd.DataFrame(results).set_index("model")
print(res_df.to_string())

"""## 7. Кросс-валидация и проверка переобучения"""

tscv = TimeSeriesSplit(n_splits=5)
cv_results = {}

model_defs = {
    "LogReg": LogisticRegression(C=0.1, max_iter=2000, class_weight="balanced",
                                 random_state=RANDOM_STATE, n_jobs=-1),
    "DecisionTree": DecisionTreeClassifier(max_depth=8, min_samples_leaf=20,
                                           class_weight="balanced", random_state=RANDOM_STATE),
    "RandomForest": RandomForestClassifier(n_estimators=200, max_depth=12, min_samples_leaf=10,
                                           class_weight="balanced", n_jobs=-1, random_state=RANDOM_STATE),
    "XGBoost": xgb.XGBClassifier(n_estimators=400, learning_rate=0.05, max_depth=6,
                                  scale_pos_weight=SCALE_POS, tree_method="hist",
                                  eval_metric="aucpr", random_state=RANDOM_STATE, n_jobs=-1, verbosity=0),
    "LightGBM": lgb.LGBMClassifier(n_estimators=400, learning_rate=0.05, max_depth=6,
                                    is_unbalance=True, random_state=RANDOM_STATE, n_jobs=-1, verbose=-1),
}

print("TimeSeriesSplit (5 фолдов) — средний CV ROC-AUC:\n")
for name, model in model_defs.items():
    Xtr_use = X_tr_sc if name == "LogReg" else X_tr.values
    fold_aucs = []
    for fold_idx, (tr_idx, va_idx) in enumerate(tscv.split(Xtr_use)):
        m = type(model)(**model.get_params())
        m.fit(Xtr_use[tr_idx], y_tr.iloc[tr_idx])
        prob = m.predict_proba(Xtr_use[va_idx])[:,1]
        if y_tr.iloc[va_idx].nunique() == 2:
            fold_aucs.append(roc_auc_score(y_tr.iloc[va_idx], prob))
    cv_results[name] = np.mean(fold_aucs) if fold_aucs else np.nan
    print(f"  {name:<15} CV ROC-AUC = {cv_results[name]:.4f}  (фолдов: {len(fold_aucs)})")
    gc.collect()

# Проверка переобучения: Train vs Test ROC-AUC
print("\nПроверка переобучения (Train vs Test ROC-AUC):\n")
overfit_results = []
for name in res_df.index:
    m, _ = models[name]
    Xtr_use = X_tr_sc if name == "LogReg" else X_tr
    train_prob = m.predict_proba(Xtr_use)[:,1]
    train_auc  = roc_auc_score(y_tr, train_prob)
    test_auc   = res_df.loc[name, "ROC_AUC"]
    gap        = train_auc - test_auc
    overfit_results.append({"model": name, "Train_AUC": round(train_auc,4),
                            "Test_AUC": test_auc, "Delta": round(gap,4)})
    flag = "переобучение" if gap > 0.10 else "переобучения нет"
    print(f"  {name:<15} Train={train_auc:.4f}  Test={test_auc:.4f}  Δ={gap:.4f}  {flag}")

overfit_df = pd.DataFrame(overfit_results).set_index("model")
print(f"признаков ({len(FEAT)}) относительно количества положительных примеров")
print(f"в train ({y_tr.sum():,}).")

"""## 8. Optuna-оптимизация LightGBM + SMOTE"""

cut_val = X_tr.index[int(len(X_tr)*0.9)]
Xvtr, Xvva = X_tr.iloc[:cut_val], X_tr.iloc[cut_val:]
yvtr, yvva = y_tr.iloc[:cut_val], y_tr.iloc[cut_val:]
print(f"Внутренняя валидация: train={len(Xvtr):,}  val={len(Xvva):,}  val_pos={yvva.sum():,}")

def lgb_objective(trial):
    params = dict(
        n_estimators      = trial.suggest_int("n_estimators", 200, 700),
        learning_rate     = trial.suggest_float("learning_rate", 0.01, 0.2, log=True),
        max_depth         = trial.suggest_int("max_depth", 4, 8),
        num_leaves        = trial.suggest_int("num_leaves", 20, 150),
        subsample         = trial.suggest_float("subsample", 0.6, 1.0),
        colsample_bytree  = trial.suggest_float("colsample_bytree", 0.6, 1.0),
        min_child_samples = trial.suggest_int("min_child_samples", 5, 100),
        reg_alpha         = trial.suggest_float("reg_alpha", 1e-3, 10, log=True),
        reg_lambda        = trial.suggest_float("reg_lambda", 1e-3, 10, log=True),
        is_unbalance=True, random_state=RANDOM_STATE, n_jobs=-1, verbose=-1,
    )
    m = lgb.LGBMClassifier(**params)
    m.fit(Xvtr, yvtr)
    return average_precision_score(yvva, m.predict_proba(Xvva)[:,1])

study = optuna.create_study(direction="maximize",
                             sampler=optuna.samplers.TPESampler(seed=RANDOM_STATE))
study.optimize(lgb_objective, n_trials=60, show_progress_bar=True)

best_params = study.best_params
best_params.update({"is_unbalance":True, "random_state":RANDOM_STATE, "n_jobs":-1, "verbose":-1})
print(f"\nBest val PR-AUC: {study.best_value:.4f}")
print(f"Best params: {best_params}")

eval_model("LightGBM_opt", lgb.LGBMClassifier(**best_params), X_tr, X_te)
gc.collect()

# SMOTE
minority_n = int((y_tr==1).sum())
k_neighbors = max(1, min(5, minority_n - 1))
target_ratio = 0.05

sm = SMOTE(random_state=RANDOM_STATE, k_neighbors=k_neighbors, sampling_strategy=target_ratio)
X_sm, y_sm = sm.fit_resample(X_tr, y_tr)
print(f"После SMOTE: {X_sm.shape}, доля flg=1: {y_sm.mean()*100:.2f}%")

smote_params = {k:v for k,v in best_params.items() if k != "is_unbalance"}
lgb_smote = lgb.LGBMClassifier(**smote_params, is_unbalance=False)
lgb_smote.fit(X_sm, y_sm)

prob_sm = lgb_smote.predict_proba(X_te)[:,1]
pred_sm = lgb_smote.predict(X_te)
r_sm = {"model": "LightGBM_SMOTE",
        "ROC_AUC":   round(roc_auc_score(y_te, prob_sm), 4),
        "PR_AUC":    round(average_precision_score(y_te, prob_sm), 4),
        "F1":        round(f1_score(y_te, pred_sm, zero_division=0), 4),
        "Recall":    round(recall_score(y_te, pred_sm, zero_division=0), 4),
        "Precision": round(precision_score(y_te, pred_sm, zero_division=0), 4)}
models["LightGBM_SMOTE"] = (lgb_smote, X_te)
results.append(r_sm)
print(f"\n  LightGBM_SMOTE   ROC={r_sm['ROC_AUC']}  PR={r_sm['PR_AUC']}  "
      f"F1={r_sm['F1']}  Recall={r_sm['Recall']}  Precision={r_sm['Precision']}")
print("\nСравнение с LightGBM_opt (без SMOTE) показывает, насколько")
print("class_weight/is_unbalance уже достаточен для коррекции дисбаланса.")
del X_sm, y_sm; gc.collect()

